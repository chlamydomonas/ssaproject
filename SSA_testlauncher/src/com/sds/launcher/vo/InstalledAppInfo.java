package com.sds.launcher.vo;

public class InstalledAppInfo {
	public String appId;
	public String appVerCode;
	public String appInstalledDate;
	
	public String getAppId() {
		return appId;
	}
	public void setAppId(String appId) {
		this.appId = appId;
	}
	public String getAppVerCode() {
		return appVerCode;
	}
	public void setAppVerCode(String appVerCode) {
		this.appVerCode = appVerCode;
	}
	public String getAppInstalledDate() {
		return appInstalledDate;
	}
	public void setAppInstalledDate(String appInstalledDate) {
		this.appInstalledDate = appInstalledDate;
	}
}
